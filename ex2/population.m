function population()
x=1990:2010;
y=[114333 115823 117171 118517 119850 121121 122389 123626 124761 125786 126743 127627 128453 129227 129988 130756 131448 132129 132802 133450 134091];
f=polyfit(x,y,2);
yi=polyval(f,[x 2011 2012]);
plot([x 2011 2012],yi,"--",x,y,[2011 2012],[134500,135400],"*");
legend('fitting','actual','actual');
text(x(8),y(8),"yi= "+num2str(f(1))+"x^2 +"+num2str(f(2))+"x "+num2str(f(3)));
text(2010,yi(21),"estimation: 2011->"+num2str(yi(22))+"    2012->"+num2str(yi(23)));
text(2012.5,135400,"actual: 2011->134500    2012->135400");
end